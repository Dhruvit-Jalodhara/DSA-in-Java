package L01_Basics_Of_Java;

public class C03_variableint {

    public static void main(String[] args) {
        int x;  // variable declare
        x = 500;  // variable intialization

        int y = 100;  // declaration and intialization
        System.out.println(x);
        System.out.println(y);
        System.out.println(x+y);
        System.out.println();


        x = 300;  // variable updation
        System.out.println(x);
        System.out.println(y+100);
        System.out.println();

        x = x + 100;
        System.out.println(x);
        System.out.println();



    }
}

