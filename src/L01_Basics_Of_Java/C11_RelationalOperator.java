package L01_Basics_Of_Java;

public class C11_RelationalOperator {

    public static void main(String[] args) {
        int x = 5 , y = 7; // = : assignment operator
        System.out.println(x>y); // x greater than y
        System.out.println(x<y);  // x less than y
        System.out.println(x>=y);  // x greater than or equal to y
        System.out.println(x<=y);  // x greater than or equal to y
        System.out.println(x==y);  // x is equal to y
        System.out.println(x!=y);  // x is not equal to y
    }
}

